            <p>Anita McCormick received her Bachelor’s degree in Fine Art at the University of Houston in 1984. While in college, she supported herself by performing landscape maintenance in the Clear Lake / League City area south of Houston. Her “part-time job” became the company, which is now Landscape Art. One of her customers was a Shell station in Dickinson, Texas. It wasn’t too long before the station caught the attention of some of the Shell corporate folks in Houston. Within a few years, Anita was designing the landscaping for all of the Houston area Shell stations and Landscape Art was installing most of them.</p>
            <p>As the company grew, the reputation of Anita and Landscape Art grew with it. Exxon decided that they were ready to improve their image and asked Anita to come up with a landscape concept that would be “different from and better than Shell”. What Anita came up with became the Exxon Signature Landscape. It was so successful that Landscape Art provided landscape and irrigation designs for all of the new Exxon retail company stores in Texas, Tennessee and Louisiana through the 1990’s. Additionally, Anita has acted as a consultant for Exxon in Dallas, Memphis, Orlando, Nashville, Washington D.C. and Baltimore.</p>
            <p>Anita was born in Alton, Illinois, has lived in the Houston area for over thirty years and is married. She is a Past-President of the Clear Lake Area Chamber of Commerce Toastmaster’s Club and is a Past-President of the Lady’s Golf Association at South Shore Harbour Country Club. She is a member of the Associated Landscape Contractors of America, Texas Landscape and Nurseryman’s Association and is a Certified Landscape Professional (CLP #336) and a Texas Licensed Irrigator (LI7824).</p>
